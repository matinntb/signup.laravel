    public function store(ExistEmailRequest $request)

    {
        $data = $request->only(['email','password']);
        $user = DB::table('users_data')
            ->select('id','email')->where('email', $data['email'])->where('password', $data['password'])
            ->get();

        $url = '/user/' . $user[0]->id . '/edit';

        return redirect($url);
    }

